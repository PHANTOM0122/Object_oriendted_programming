#include <iostream>
using namespace std;

int main() {

	int x, y;
	cout << "Please enter two integar values: ";
	cin >> x >> y;
	cout << x << "+" << y << "=" << x + y << "\n";
	cout << x << "-" << y << "=" << x - y << "\n";
	cout << x << "*" << y << "=" << x * y << "\n";
	cout << x << "/" << y << "=" << x / y << "\n";
	cout << x << "%" << y << "=" << x % y << "\n";
}
